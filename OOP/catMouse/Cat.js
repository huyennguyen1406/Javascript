class Cat {
    name
    weight
    maxSpeed
    sound
    constructor(name, weight, maxSpeed, sound) {
        this.name = name;
        this.weight = weight;
        this.maxSpeed = maxSpeed;
        this.sound = sound;
    }
    setName(name) {
        this.name = name;
    }
    setWeight(weight) {
        this.weight = weight;
    }
    setMaxSpeed(maxSpeed) {
        this.maxSpeed = maxSpeed;
    }
    setSound(sound) {
        this.sound = sound;
    }

}
